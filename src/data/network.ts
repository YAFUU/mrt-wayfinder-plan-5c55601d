function toStations(lineId: LineId, rows: Row[], source: DataSourceMetadata): MrtStation[] {
  return rows.map((r, i) => ({
    id: r[0],
    lineId,
    code: r[0],
    order: i + 1,
    nameTh: r[1],
    nameEn: r[2],
    lat: r[3],
    lng: r[4],
    isInterchange: r[5] ?? false,
    operational: true,
    accessibility: { elevator: true, ramp: true, tactile: true },
    source,
  }));
}

