/** Data coverage validation — run at app boot for warnings. */
export function validateDataset(): string[] {
  const errors: string[] = [];
  const ids = new Set<string>();
  for (const s of STATIONS) {
    if (ids.has(s.id)) errors.push(`Duplicate station id: ${s.id}`);
    ids.add(s.id);
    if (!s.nameTh || !s.nameEn) errors.push(`Missing name on ${s.id}`);
    if (!s.lat || !s.lng) errors.push(`Missing coords on ${s.id}`);
  }
  for (const [a, b] of INTERCHANGES) {
    if (!ids.has(a)) errors.push(`Interchange references missing station: ${a}`);
    if (!ids.has(b)) errors.push(`Interchange references missing station: ${b}`);
  }
  return errors;
}
